// Define the numbers and the operation here
let firstNumber = 10
let secondNumber = 2
let operation = "/"
// Write if-statements here
if (operation === "+"){
    console.log (`${firstNumber} + ${secondNumber} = ${firstNumber + secondNumber}`)}
else if (operation === "-"){
    console.log (`${firstNumber} - ${secondNumber} = ${firstNumber - secondNumber}`)}
else if (operation === "*"){ 
    console.log (`${firstNumber} * ${secondNumber} = ${firstNumber * secondNumber}`)}
else if (operation === "/"){
    console.log (`${firstNumber} / ${secondNumber} = ${firstNumber / secondNumber}`)
    
}